#include "ft_printf.h"

size_t	ft_nbrlen(int nbr)
{
	size_t	i;

	i = 0;
	if (nbr <= 0)
	{
		i++;
	}
	while (nbr != 0)
	{
		nbr /= 10;
		i++;
	}
	return (i);
}
